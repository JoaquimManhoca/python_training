mytuple = ("apple", "banana", "cherry")

#Allow Duplicates
thistuple = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple)

#Tuple Length
print(len(thistuple))

#Create Tuple With One Item
name = ('joaquim',)
print(f"{name} is type {type(name)}")

#Tuple Items - Data Types
