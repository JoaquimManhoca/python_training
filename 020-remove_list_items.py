#Remove Specified Item
fruts = ['orange', 'mango', 'apple']
fruts.remove('apple')
print(fruts)

#Remove Specified Index
fruts.extend(['watermelon', 'Tomatos', 'banana', 'uva'])
fruts.pop(2)
print(fruts)

#delete all list
del fruts

#Clear the List
fruts = (['watermelon', 'Tomatos', 'banana', 'uva'])
fruts.clear()